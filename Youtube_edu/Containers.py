# Containers
my_list1 = [10,10.1,'python',True]
print(my_list1)
print(type(my_list1))

my_list2 = list([1,2,3]) # LIST함수로 값을 넣어줌
print(my_list2)


my_tuple1 = (10,10.1,'python',True)
print(my_tuple1)
print(type(my_tuple1))

my_tuple2 = tuple([1,2,3]) # tuple함수로 중괄호안에 list형식으로 값을 넣어줌
print(my_tuple2)